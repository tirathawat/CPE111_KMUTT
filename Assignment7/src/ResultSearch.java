class ResultSearch {
    private Bnode result;//The node that points to the search result.
    private int index;//Index of search results in tree set.


    //Getter and Setter
    Bnode getResult() {
        return result;
    }

    void setResult(Bnode result) {
        this.result = result;
    }

    int getIndex() {
        return index;
    }

    void setIndex(int index) {
        this.index = index;
    }
}
