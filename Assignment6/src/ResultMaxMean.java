class ResultMaxMean {
    private int count;//Number of words with the most meaning.
    private int index;//Starting index of words with the most meaning.

    //Getter and Setter
    void setCount(int count) {
        this.count = count;
    }

    void setIndex(int index) {
        this.index = index;
    }

    int getCount() {
        return count;
    }

    int getIndex() {
        return index;
    }
}
